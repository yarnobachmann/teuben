		<h3>Financial Lease</h3>
		<p>Financiële leasing is een leasevorm waarbij de kredietverstrekker of lessor enkel juridisch eigenaar blijft terwijl het volledige economisch eigendom (het genot, de verzekering, het onderhoud) overgaat op de kredietnemer of lessee. De lessor loopt daarbij het risico van waardevermindering. Het grootste verschil tussen financiële en operationele leasing is dat er bij de eerste het eigendom in principe bij de lessee ligt, waar bij operationele leasing een zakelijke vorm van huren is.
		 Financiële leasing is een product dat vaak gebruikt wordt voor de aanschaf van een auto, bedrijfswagen of vrachtwagen. Bij sommige aanbieders is het ook mogelijk zaken als computers, machines, scooters, motors en overige bedrijfsmiddelen met serienummer te financieren.</p>
